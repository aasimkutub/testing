package com.tthings.remote_application.viewModel;

public class CustomButton {

    private String key;
    private String value;

    public CustomButton() {
        key = null;
        value = null;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
